const CUSTOM_EXPERTS_KEY = 'roundtable_custom_experts';

export function loadCustomExperts(): Expert[] {
  try {
    const raw = localStorage.getItem(CUSTOM_EXPERTS_KEY);
    return raw ? JSON.parse(raw) : [];
  } catch {
    return [];
  }
}

export function saveCustomExpert(expert: Expert): void {
  const existing = loadCustomExperts();
  const updated = [...existing.filter((e) => e.id !== expert.id), expert];
  localStorage.setItem(CUSTOM_EXPERTS_KEY, JSON.stringify(updated));
}

export function deleteCustomExpert(id: string): void {
  const existing = loadCustomExperts();
  localStorage.setItem(CUSTOM_EXPERTS_KEY, JSON.stringify(existing.filter((e) => e.id !== id)));
}
